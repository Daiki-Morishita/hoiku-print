def wait_for_image(page, timeout=240):
    deadline = time.time() + timeout
    while time.time() < deadline:
        imgs = page.query_selector_all("img")
        for img in imgs:
            src = img.get_attribute("src") or ""
            if "backend-api/estuary/content" in src or "oaiusercontent" in src:
                return ('ok', src)
        try:
            body_text = page.evaluate("() => document.body.innerText")
            # コンテンツポリシー拒否を優先検知（タイムアウト240秒を待たずに即返す）
            for phrase in POLICY_PHRASES:
                if phrase in body_text:
                    log(f"  ⚠️ コンテンツポリシー検知: \"{phrase}\"")
                    return ('policy', None)
            for phrase in ERROR_PHRASES:
                if phrase in body_text:
                    # UI に再開時刻が含まれていれば抽出してログ出力
                    # 例: "20:14 以降にもう一度お試しください"
                    m = re.search(r'(\d{1,2}:\d{2})\s*以降', body_text)
                    if m:
                        log(f"  ⏰ ChatGPT再開時刻（UI表示）: {m.group(1)} 以降")
                        notify_mac("hoiku-print", f"制限中 → {m.group(1)}以降に再開可能")
                    return ('error', None)
        except Exception:
            pass
        time.sleep(3)
    return ('timeout', None)


