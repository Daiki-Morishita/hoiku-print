def log(msg):
    ts = time.strftime("%H:%M:%S")
    line = f"[{ts}] {msg}"
    print(line, flush=True)
    with open(LOG_FILE, "a") as f:
        f.write(line + "\n")


def supabase_client():
    return create_client(SUPABASE_URL, SUPABASE_KEY)


def upload_to_supabase(client, local_path, remote_name):
    with open(local_path, "rb") as f:
        data = f.read()
    client.storage.from_(BUCKET).upload(
        path=remote_name,
        file=data,
        file_options={"content-type": "image/png", "upsert": "true"},
    )
    return f"{SUPABASE_BASE}/{remote_name}"


def add_to_data_ts(item_id, theme_type, variant, vdata, supabase_urls):
    levels = ["simple", "easy", "normal", "rich"]
    content = DATA_TS.read_text()

    # 全levelが既存かチェック（全部あればスキップ）
    all_exist = all(f"id: '{item_id}-{lv}-{variant}'" in content for lv in levels)
    if all_exist:
        log(f"  data.ts に全level既存: {item_id}-{variant} — スキップ")
        return False

    tag_map = {"park": "こうえん", "dinosaurs": "きょうりゅう", "fruits": "くだもの", "vegetables": "やさい"}
    tag_label = tag_map.get(theme_type, theme_type)
    idea_map = {
        "park": "['好きな色で塗ってみよう', '公園で遊んだことを思い出しながら塗ろう']",
        "fruits": "['好きな色で塗ってみよう', 'どんな味か考えながら塗ろう']",
        "vegetables": "['好きな色で塗ってみよう', 'どんな料理になるか考えながら塗ろう']",
    }
    activity_ideas = idea_map.get(theme_type, "['好きな色で塗ってみよう', '背景もカラフルに仕上げよう']")

    block = ""
    for lv in levels:
        file_id = f"{item_id}-{lv}-{variant}"
        if file_id not in supabase_urls:
            continue
        # 既にdata.tsに存在するlevelは重複追加しない
        if f"id: '{file_id}'" in content:
            log(f"  data.ts に既存: {file_id} — スキップ")
            continue
        url = supabase_urls[file_id]
        title = vdata["titles"][lv]
        desc  = vdata["descs"][lv]
        age_min, age_max, diff, dur, tools = LEVEL_PARAMS[lv]
        block += f"""  {{
    id: '{file_id}',
    title: '{title}',
    description: '{desc}',
    ageMin: {age_min}, ageMax: {age_max}, difficulty: {diff}, duration: {dur},
    category: 'coloring', theme: '{theme_type}',
    tags: ['{item_id}', '{tag_label}', 'ぬりえ'],
    tools: ['{tools}'],
    activityIdeas: {activity_ideas},
    imageUrl: '{url}',
    illustUrl: '{url}',
    illustVersion: 1,
    imageStatus: 'pending_review',
    pdfUrl: '',
    createdAt: '{time.strftime('%Y-%m-%dT%H:%M')}',
    popular: false,
  }},
"""
    insert_pos = content.rfind('},', 0, content.find(']\n\nexport type SortKey')) + 2
    new_content = content[:insert_pos] + '\n' + block.rstrip(',\n') + ',' + content[insert_pos:]
    DATA_TS.write_text(new_content)
    log("  data.ts 更新完了")
    return True


def git_commit(item_id, theme_type, variant):
    """commitのみ（push不要な--allモード用）"""
    subprocess.run(["git", "add", "src/lib/data.ts"], cwd=REPO_DIR, check=True)
    msg = f"Add {theme_type}/{item_id}-{variant} coloring pages"
    subprocess.run(["git", "commit", "-m", msg], cwd=REPO_DIR, check=True)
    log(f"  commit 完了: {item_id}-{variant}")


def git_push():
    """バッチ終了時に1回だけ呼ぶ"""
    subprocess.run(["git", "pull", "--rebase", "origin", "main"], cwd=REPO_DIR, check=False)
    subprocess.run(["git", "push", "origin", "main"], cwd=REPO_DIR, check=True)
    log("  push 完了")


