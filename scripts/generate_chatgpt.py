_TONE_GROUPS = {
    'A_command':  [6, 8, 9, 18],        # 命令・指示系
    'B_polite':   [1, 7, 11, 13, 16],   # 丁寧依頼系
    'C_casual':   [2, 5, 19],           # カジュアル系
    'D_business': [4, 10, 15],          # ビジネス・文書系
    'E_flat':     [0, 3, 12, 14, 17],   # フラット・中性系
}
_RECENT_GROUPS: list = []  # 直近4件の使用グループ履歴


def _pick_style() -> int:
    """
    直近4件で使ったグループを除外してスタイルを選ぶ。
    5グループ × 除外4件 → 常に1グループ以上が選択可能。
    結果として、連続5回の中に同一口調グループは出現しない。
    """
    global _RECENT_GROUPS
    excluded = set(_RECENT_GROUPS[-4:])
    available = [g for g in _TONE_GROUPS if g not in excluded]
    chosen_group = random.choice(available)
    style = random.choice(_TONE_GROUPS[chosen_group])
    _RECENT_GROUPS.append(chosen_group)
    if len(_RECENT_GROUPS) > 4:
        _RECENT_GROUPS.pop(0)
    log(f"  🎨 プロンプトスタイル: {chosen_group} / style {style}")
    return style


def _simplify_scene(scene: str) -> str:
    """
    コンテンツポリシー対策: シーンの複雑な修飾を削ぎ落とす。
    - 複数文の場合は1文目のみ残す
    - 「にぎやかな構図」「〜が並ぶ」などの複雑さを示す語を除去
    """
    sentences = [s.strip() for s in scene.split('。') if s.strip()]
    if len(sentences) > 1:
        simplified = sentences[0] + '。'
        log(f"  🔧 シーン簡略化: {scene[:30]}... → {simplified}")
        return simplified
    # 1文でも複雑な表現を削ぎ落とす
    s = re.sub(r'[、。]*(にぎやかな|がいっぱい|複数の|たくさんの|〜が並ぶ)[^。]*', '', scene).strip()
    if s and s != scene:
        if not s.endswith('。'):
            s += '。'
        log(f"  🔧 シーン簡略化: {scene[:30]}... → {s}")
        return s
    return scene


def build_prompt(scene: str, note: str, cond_items: list) -> str:
    """
    scene / note / cond_items から 20スタイルのうち1つをランダム選択してプロンプトを生成。
    指示内容・クオリティ要件は完全同一、表現形式のみが毎回変わる。
    口調グループ（5種）は直近4件と被らないよう制御される。
    """
    style = _pick_style()
    items = cond_items[:]

    def plain():    return '\n'.join(items)
    def bullet():   return '\n'.join(f'・{c}' for c in items)
    def numbered(): return '\n'.join(f'{i+1}. {c}' for i, c in enumerate(items))
    def dashed():   return '\n'.join(f'- {c}' for c in items)
    def flat():     return '、'.join(items)

    def sh_bullet():
        s = items[:]; random.shuffle(s)
        return '\n'.join(f'・{c}' for c in s)
    def sh_numbered():
        s = items[:]; random.shuffle(s)
        return '\n'.join(f'{i+1}. {c}' for i, c in enumerate(s))
    def sh_dashed():
        s = items[:]; random.shuffle(s)
        return '\n'.join(f'- {c}' for c in s)

    if style == 0:
        # 標準型（改行区切り・共通条件ヘッダー）
        return f"{scene}\n{note}\n共通条件\n{plain()}"

    elif style == 1:
        # 丁寧なお願い文・bullet・条件→内容の順
        return (
            f"お願いがあるのですが、以下の仕様で塗り絵イラストを作成していただけますか？\n\n"
            f"仕様：\n{bullet()}\n\n"
            f"内容：{scene}\n補足：{note}\n\nよろしくお願いします。"
        )

    elif style == 2:
        # カジュアル依頼・番号リスト・条件先行
        return (
            f"これ作ってもらえる？\n\n"
            f"守ってほしいこと：\n{numbered()}\n\n"
            f"内容：{scene}\nスタイル：{note}"
        )

    elif style == 3:
        # フラット一段落・読点つなぎ条件
        return f"{scene}\n{note}\n条件：{flat()}"

    elif style == 4:
        # ビジネス文書スタイル・dash・条件ブロック先頭
        return (
            f"制約事項：\n{dashed()}\n\n"
            f"描画対象：{scene}\n"
            f"補足：{note}"
        )

    elif style == 5:
        # チャット風・シャッフルbullet
        return (
            f"{scene}\n{note}\n\n"
            f"条件はこちら：\n{sh_bullet()}"
        )

    elif style == 6:
        # 命令形・シャッフル番号リスト
        return (
            f"以下を守って塗り絵イラストを描くこと。\n\n"
            f"{sh_numbered()}\n\n"
            f"テーマ：{scene}\n{note}"
        )

    elif style == 7:
        # ですます・改行少なめ
        cond_inline = '。'.join(items) + '。'
        return (
            f"こちらの塗り絵イラストを描いてください。\n\n"
            f"{scene}{note}"
            f"{cond_inline}"
        )

    elif style == 8:
        # 「指示があります」から始まる
        return (
            f"指示があります。\n\n"
            f"内容：{scene}\n{note}\n\n"
            f"要件：\n{dashed()}"
        )

    elif style == 9:
        # 「以上実行してください」で締める
        return (
            f"{scene}\n{note}\n\n"
            f"次の条件をすべて満たすこと：\n{bullet()}\n\n"
            f"以上実行してください。"
        )

    elif style == 10:
        # 「以下に仕様を指定します」・番号リスト
        return (
            f"以下に仕様を指定します。\n\n"
            f"テーマ：{scene}\n"
            f"スタイル補足：{note}\n\n"
            f"技術要件：\n{numbered()}"
        )

    elif style == 11:
        # 条件ブロック先・シャッフルdash・内容あと
        return (
            f"次の要件をすべて満たしてください。\n{sh_dashed()}\n\n"
            f"上記を踏まえて、{scene}\n（{note}）"
        )

    elif style == 12:
        # 全インライン・改行なし
        cond_inline = '。'.join(items) + '。'
        return f"{scene} {note} {cond_inline}"

    elif style == 13:
        # 「ご確認のうえ」・丁寧・【セクション】bullet
        return (
            f"以下の要件をご確認のうえ、塗り絵イラストを1点生成してください。\n\n"
            f"【テーマ】{scene}\n"
            f"【スタイル】{note}\n"
            f"【制約】\n{bullet()}"
        )

    elif style == 14:
        # ミニマル・スラッシュ区切り
        return f"{scene} / {note} / " + ' / '.join(items)

    elif style == 15:
        # 仕様書（Markdown見出し）スタイル
        return (
            f"# 塗り絵イラスト仕様\n\n"
            f"## テーマ\n{scene}\n\n"
            f"## スタイル補足\n{note}\n\n"
            f"## 制約条件\n{numbered()}"
        )

    elif style == 16:
        # 「これらをやっていただけますか？」アウトロ
        return (
            f"内容：{scene}\n補足：{note}\n\n"
            f"条件：\n{sh_bullet()}\n\n"
            f"これらをやっていただけますか？"
        )

    elif style == 17:
        # 条件を括弧内インライン
        cond_paren = '（' + '・'.join(items) + '）'
        return f"{scene}\n{note}\n{cond_paren}"

    elif style == 18:
        # 「次の要件で」・dash・条件先
        return (
            f"次の要件でイラストを出力してください。\n\n"
            f"要件：\n{dashed()}\n\n"
            f"テーマ：{scene}（{note}）"
        )

    else:  # style == 19
        # カジュアル・ひらがな混じり
        return (
            f"ぬりえのイラストをかいてほしいです！\n\n"
            f"テーマ：{scene}\n"
            f"スタイルのほそく：{note}\n\n"
            f"きまり：\n{bullet()}\n\n"
            f"おねがいします"
        )


def attach_rate_limit_guard(page):
    """
    ページに 429 Fail-Fast リスナーを1回だけ張る。
    429 検知した瞬間にログ・通知して os._exit(1) で終了する。
    （sys.exit はコールバックスレッドから抜けられないので os._exit を使用）
    """
    def _on_response(response):
        if response.status == 429:
            retry_raw = response.headers.get("retry-after", "")
            try:
                secs = int(retry_raw)
                hint = f"{secs // 3600}h{(secs % 3600) // 60}m"
            except (ValueError, TypeError):
                hint = "不明"
            log(f"  🔴 HTTP 429 Fail-Fast: {response.url[:70]}")
            log(f"     Retry-After = {retry_raw or '(なし)'}  ({hint})")
            notify_mac("hoiku-print", f"429検知 → 停止 (Retry-After: {hint})")
            os._exit(1)

    page.on("response", _on_response)


def whiten_background(in_path, out_path=None, threshold=235):
    try:
        from PIL import Image
        import numpy as np
        out_path = out_path or in_path
        img = Image.open(in_path).convert("RGB")
        arr = np.array(img)
        mask = (arr[:,:,0] >= threshold) & (arr[:,:,1] >= threshold) & (arr[:,:,2] >= threshold)
        arr[mask] = [255, 255, 255]
        Image.fromarray(arr).save(out_path, "PNG", optimize=True)
        return True
    except Exception as e:
        log(f"  背景純白化失敗: {e}")
        return False


def download_image(page, img_src, out_path):
    data = page.evaluate("""async (url) => {
        const r = await fetch(url, {credentials: 'include'});
        const buf = await r.arrayBuffer();
        return Array.from(new Uint8Array(buf));
    }""", img_src)
    if data:
        with open(out_path, "wb") as f:
            f.write(bytes(data))
        whiten_background(out_path)
        return True
    return False


def delete_current_chat(page):
    try:
        url = page.url
        m = re.search(r"/c/([0-9a-f-]+)", url)
        if not m:
            log(f"  [削除スキップ] URL に会話IDなし: {url[:60]}")
            return False
        conv_id = m.group(1)
        result = page.evaluate("""async (id) => {
            const r = await fetch(`/backend-api/conversation/${id}`, {
                method: 'PATCH',
                credentials: 'include',
                headers: {'Content-Type': 'application/json'},
                body: JSON.stringify({is_visible: false}),
            });
            return {ok: r.ok, status: r.status};
        }""", conv_id)
        if result and result.get('ok'):
            log(f"  [削除OK] {conv_id[:8]}...")
            return True
        else:
            log(f"  [削除失敗] status={result.get('status') if result else 'None'} id={conv_id[:8]}...")
            return False
    except Exception as e:
        log(f"  [削除例外] {e}")
        return False


def cleanup_old_chats(page, threshold=20):
    """セッション数がthresholdを超えたら古いものから削除する"""
    try:
        result = page.evaluate("""async (threshold) => {
            const r = await fetch(`/backend-api/conversations?offset=0&limit=50`, {
                credentials: 'include',
            });
            if (!r.ok) return {count: 0, deleted: 0};
            const data = await r.json();
            const items = data.items || [];
            if (items.length <= threshold) return {count: items.length, deleted: 0};
            // 古い順（末尾）から削除してthresholdまで減らす
            const toDelete = items.slice(threshold);
            let deleted = 0;
            for (const item of toDelete) {
                const dr = await fetch(`/backend-api/conversation/${item.id}`, {
                    method: 'PATCH',
                    credentials: 'include',
                    headers: {'Content-Type': 'application/json'},
                    body: JSON.stringify({is_visible: false}),
                });
                if (dr.ok) deleted++;
            }
            return {count: items.length, deleted};
        }""", threshold)
        if result and result.get('error'):
            log(f"  [セッション整理失敗] API status={result['error']}")
        elif result:
            log(f"  [セッション整理] {result['count']}件検出 → {result['deleted']}件削除")
    except Exception as e:
        log(f"  [セッション整理例外] {e}")


def jitter_sleep(current_max, rate_limited=False, success_count=0):
    """
    可変バックオフ＋ジッター（慣らし運転対応）

    success_count: これまでの連続成功数
      0〜1回目 : 慣らし P1 — 5〜10分（リクエスト直後に制限が来ないかを様子見）
      2〜4回目 : 慣らし P2 — 2〜3分（徐々に短縮）
      5回目以降: 通常運転 — INTER_REQUEST_SLEEP_MIN 〜 current_max
    rate_limited=True の場合は current_max を2倍に拡大して待機（既存ロジック）
    """
    # --daily モード: スケジュール済みギャップを優先使用
    if not rate_limited and _DAILY_SCHEDULE:
        wait = _DAILY_SCHEDULE.pop(0)
        log(f"  📅 スケジュール待機: {wait:.0f}秒 ({wait/60:.1f}分)")
        time.sleep(wait)
        return current_max

    if rate_limited:
        current_max = min(current_max * 2, INTER_REQUEST_SLEEP_MAX_CAP)
        log(f"  レート制限検知→バックオフ拡大: 上限 {current_max}秒")
        wait = random.uniform(INTER_REQUEST_SLEEP_MIN, current_max)
        log(f"  インターバル待機: {wait:.1f}秒 (上限{current_max}s)")
    elif success_count < 2:
        wait = random.uniform(300, 600)   # P1: 5〜10分
        log(f"  🐢 慣らし運転 P1 ({success_count + 1}回目成功): {wait:.0f}秒待機")
    elif success_count < 5:
        wait = random.uniform(120, 180)   # P2: 2〜3分
        log(f"  🐢 慣らし運転 P2 ({success_count + 1}回目成功): {wait:.0f}秒待機")
    else:
        wait = random.uniform(INTER_REQUEST_SLEEP_MIN, current_max)
        log(f"  インターバル待機: {wait:.1f}秒 (上限{current_max}s)")
    time.sleep(wait)
    return current_max


def generate_one(page, file_id, prompt, out_path, max_retries=3, interval_max=None, success_count=0):
    if out_path.exists():
        log(f"  スキップ（既存）: {file_id}")
        return True, interval_max or INTER_REQUEST_SLEEP_MAX

    if interval_max is None:
        interval_max = INTER_REQUEST_SLEEP_MAX

    policy_error_count = 0  # コンテンツポリシー拒否の累計回数

    for attempt in range(1, max_retries + 1):
        log(f"  生成開始 (試行 {attempt}/{max_retries}): {file_id}")
        page.goto("https://chatgpt.com/", wait_until="domcontentloaded")
        human_pause(1.8, 4.0)  # ページ表示後のランダム待機
        try:
            box = page.wait_for_selector("#prompt-textarea", timeout=30000)
        except Exception as e:
            log(f"  入力欄取得失敗: {e}")
            time.sleep(5)
            continue
        human_move_and_click(page, box)  # マウスを自然に動かしてクリック
        human_pause(0.2, 0.6)
        box.evaluate("el => el.innerHTML = ''")
        # ポリシー拒否が1回以上あり最終試行 → シーンを簡略化して挑む
        current_prompt = prompt
        if policy_error_count > 0 and attempt == max_retries:
            current_prompt = dict(prompt)
            current_prompt['scene'] = _simplify_scene(prompt['scene'])
        varied = build_prompt(current_prompt['scene'], current_prompt['note'], current_prompt['cond_items'])
        human_type(page, varied)         # ランダム速度でタイピング
        human_pause(0.4, 1.5)            # Enterを押す前のひと呼吸
        page.keyboard.press("Enter")
        log("  プロンプト送信。生成待ち...")

        status, img_src = wait_for_image(page, timeout=240)
        if status == 'ok':
            ok = download_image(page, img_src, out_path)
            if ok:
                log(f"  ローカル保存: {out_path}")
                delete_current_chat(page)
                interval_max = jitter_sleep(interval_max, rate_limited=False, success_count=success_count)
                return True, interval_max
            log("  DL失敗、リトライ")
        elif status == 'policy':
            policy_error_count += 1
            log(f"  ⚠️ ポリシー拒否 ({policy_error_count}回目)、スタイル変更してリトライ")
            delete_current_chat(page)
            time.sleep(5)
        elif status == 'error':
            log("  ChatGPTエラー検知、リトライ")
            delete_current_chat(page)
            interval_max = jitter_sleep(interval_max, rate_limited=True, success_count=success_count)
        else:
            log("  タイムアウト、リトライ")
        time.sleep(5)

    log(f"  最大リトライ到達、失敗: {file_id}")
    return False, interval_max


def notify_mac(title, msg):
    try:
        subprocess.run(["osascript", "-e", f'display notification "{msg}" with title "{title}"'], check=False)
    except Exception:
        pass


def restart_chrome(pw):
    log("  [復旧] Chromeを再起動中...")
    subprocess.run(["pkill", "-f", "remote-debugging-port=9222"], check=False)
    time.sleep(3)
    subprocess.Popen(CHROME_CMD, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    time.sleep(8)
    for attempt in range(3):
        try:
            browser = pw.chromium.connect_over_cdp("http://localhost:9222")
            log("  [復旧] Chrome再接続成功")
            return browser
        except Exception as e:
            log(f"  [復旧] 再接続失敗 ({attempt+1}/3): {e}")
            time.sleep(5)
    log("  [復旧] Chrome再接続できず")
    return None


def generate_with_recovery(pw, state, file_id, prompt, out_path):
    sc = state.get('success_count', 0)
    ok, state['interval_max'] = generate_one(state['page'], file_id, prompt, out_path, interval_max=state['interval_max'], success_count=sc)
    if ok:
        return True

    log(f"  [復旧①] 60秒クールダウン後に再試行: {file_id}")
    time.sleep(60)
    ok, state['interval_max'] = generate_one(state['page'], file_id, prompt, out_path, max_retries=1, interval_max=state['interval_max'], success_count=sc)
    if ok:
        return True

    log(f"  [復旧②] ブラウザ再起動後に再試行: {file_id}")
    new_browser = restart_chrome(pw)
    if new_browser is not None:
        try:
            state['page'].close()
        except Exception:
            pass
        state['browser'] = new_browser
        state['page'] = new_browser.contexts[0].new_page()
        attach_rate_limit_guard(state['page'])  # 新ページにもリスナーを再設定
        ok, state['interval_max'] = generate_one(state['page'], file_id, prompt, out_path, max_retries=1, interval_max=state['interval_max'], success_count=sc)
        if ok:
            return True

    log(f"  [復旧③] スキップして次へ: {file_id}")
    with open(FAILED_LOG, "a") as f:
        f.write(f"{time.strftime('%Y-%m-%dT%H:%M:%S')} {file_id}\n")
    notify_mac("ぬりえ生成失敗", f"{file_id} をスキップしました")
    return False


