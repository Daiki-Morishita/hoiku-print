def get_api_key():
    path = os.path.join(os.path.dirname(__file__), "../.env.local")
    if os.path.exists(path):
        with open(path) as f:
            for line in f:
                if line.startswith("OPENAI_API_KEY="):
                    return line.strip().split("=", 1)[1]
    return os.environ.get("OPENAI_API_KEY")


def add_to_data_ts(theme_id, entries):
    path = DATA_TS_PATH
    with open(path, 'r') as f:
        content = f.read()
    block = ""
    for file_id, title, desc, age_min, age_max, diff, dur, tools in entries:
        tag_base = theme_id.replace('-', '')
        block += f"""  {{
    id: '{file_id}',
    title: '{title}',
    description: '{desc}',
    ageMin: {age_min}, ageMax: {age_max}, difficulty: {diff}, duration: {dur},
    category: 'coloring', theme: 'vehicles',
    tags: ['{theme_id}', 'のりもの', 'ぬりえ'],
    tools: ['{tools.split("・")[0]}'],
    activityIdeas: [
      '好きな色で塗ってみよう',
      '背景もカラフルに仕上げよう',
    ],
    imageUrl: '/materials/{file_id}-illust.png',
    illustUrl: '/materials/{file_id}-illust.png',
    illustVersion: 1,
    imageStatus: 'pending_review',
    pdfUrl: '',
    createdAt: '2026-05-12',
    popular: false,
  }},\n"""
    insert_pos = content.rfind('},', 0, content.find(']\n\nexport type SortKey'))
    insert_pos += 2
    new_content = content[:insert_pos] + '\n' + block.rstrip(',\n') + ',' + content[insert_pos:]
    with open(path, 'w') as f:
        f.write(new_content)


def git_commit_push(theme_id, file_ids):
    patterns = [f"public/materials/{fid}-illust.png" for fid in file_ids]
    subprocess.run(["git", "add"] + patterns + ["src/lib/data.ts"], cwd=REPO_DIR, check=True)
    subprocess.run(["git", "commit", "-m", f"Add {theme_id} theme (4 difficulty levels)"], cwd=REPO_DIR, check=True)
    subprocess.run(["git", "push", "origin", "main"], cwd=REPO_DIR, check=True)
    print(f"  Pushed: {theme_id}")


def main():
    api_key = get_api_key()
    if not api_key:
        print("ERROR: OPENAI_API_KEY not found"); sys.exit(1)
    client = OpenAI(api_key=api_key)

    for theme in THEMES:
        tid = theme["id"]
        print(f"\n{'='*50}")
        print(f"Theme: {tid} ({theme['title_jp']})")

        file_ids = []
        for file_id, description in theme["specs"]:
            prompt = f"{description}\n{theme['common_note']}\n{COMMON_CONDITIONS}"
            out_path = os.path.join(MATERIALS_DIR, f"{file_id}-illust.png")
            print(f"  Generating {file_id}...")
            response = client.images.generate(
                model="gpt-image-2",
                prompt=prompt,
                size="1536x1024",
                quality="medium",
                n=1,
                output_format="png",
            )
            img_data = base64.b64decode(response.data[0].b64_json)
            with open(out_path, "wb") as f:
                f.write(img_data)
            file_ids.append(file_id)
            print(f"    Saved: {file_id}-illust.png")

        add_to_data_ts(tid, theme["entries"])
        git_commit_push(tid, file_ids)
        print(f"  ✓ {tid} complete")

    print("\n\nAll themes done!")


if __name__ == "__main__":
    main()
