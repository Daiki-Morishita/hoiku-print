/** クエリを正規化（漢字→よみ→ひらがなに統一） */
export function normalizeQuery(str: string): string {
  let s = str.trim().toLowerCase()
  // 漢字を置換
  for (const [kanji, hira] of Object.entries(KANJI_MAP)) {
    s = s.replaceAll(kanji, hira)
  }
  // カタカナをひらがなに
  s = kanaToHira(s)
  return s
}

/** テキストを検索用に正規化 */
export function normalizeText(str: string): string {
  return kanaToHira(str.toLowerCase())
}
